package org.tutorial.javabrains.messenger.model;

public class Link {
	
	/*--------------------------Private Class Variables--------------------------*/

	private String link;
	private String rel;
	
	/*------------------------------Getters/Setters------------------------------*/
	
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getRel() {
		return rel;
	}
	public void setRel(String rel) {
		this.rel = rel;
	}
	
	
}
